package GenericExercise;

public class Age1 {
	int ageInt;
	String ageString;
	double ageDouble;

	public void set_age_int(int age) {
		this.ageInt = age;
	}

	public void set_age_String(String age) {
		this.ageString = age;
	}

	public void set_age_double(double age) {
		this.ageDouble = age;
	}

	public int get_age_int() {
		return ageInt;
	}

	public String get_age_String() {
		return ageString;
	}

	public double get_age_double() {
		return ageDouble;
	}

}
